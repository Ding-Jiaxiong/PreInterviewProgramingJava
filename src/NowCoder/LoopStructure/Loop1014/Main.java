package NowCoder.LoopStructure.Loop1014;

import java.util.Scanner;

public class Main {

    public static void main(String[] args) {

        Scanner in = new Scanner(System.in);

        int num = in.nextInt();

        while (num-- != 0) {

            int a = in.nextInt();
            int b = in.nextInt();

            System.out.println(a + b);

        }

    }

}
